package model;

/**
 *
 * @author hasot
 */
public class CartDTO {
    private int cartId;
    private String userId;

    public CartDTO() {
    }

    public CartDTO(int cartId, String userId) {
        this.cartId = cartId;
        this.userId = userId;
    }

    public int getCartId() {
        return cartId;
    }

    public void setCartId(int cartId) {
        this.cartId = cartId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

     
}
